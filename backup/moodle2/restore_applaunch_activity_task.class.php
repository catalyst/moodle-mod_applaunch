defined('MOODLE_INTERNAL') || die();

global $CFG;

require_once($CFG->dirroot . '/mod/applaunch/backup/moodle2/restore_applaunch_stepslib.php');

class restore_applaunch_activity_task extends restore_activity_task {

    /**
     * Define (add) particular settings this activity can have
     */
    protected function define_my_settings() {
        // No particular settings for this activity.
    }

    /**
     * Define (add) particular steps this activity can have
     */
    protected function define_my_steps() {
        // Label only has one structure step.
        $this->add_step(new restore_applaunch_activity_structure_step('applaunch_structure', 'applaunch.xml'));
    }

    /**
     * Define the contents in the activity that must be
     * processed by the link decoder
     */
    public static function define_decode_contents() {
        return [];
    }

    /**
     * Define the decoding rules for links belonging
     * to the activity to be executed by the link decoder
     */
    public static function define_decode_rules() {
        $rules = [];

        $rules[] = new restore_decode_rule('APPLAUNCHVIEWBYID', '/mod/applaunch/view.php?id=$1', 'course_module');
        $rules[] = new restore_decode_rule('APPLAUNCHINDEX', '/mod/applaunch/index.php?id=$1', 'course');

        return $rules;
    }

    /**
     * Define the restore log rules that will be applied
     * by the {@link restore_logs_processor} when restoring
     * basiclti logs. It must return one array
     * of {@link restore_log_rule} objects
     */
    public static function define_restore_log_rules() {
        $rules = [];

        $rules[] = new restore_log_rule('applaunch', 'add', 'view.php?id={course_module}', '{applaunch}');
        $rules[] = new restore_log_rule('applaunch', 'update', 'view.php?id={course_module}', '{applaunch}');
        $rules[] = new restore_log_rule('applaunch', 'view', 'view.php?id={course_module}', '{applaunch}');

        return $rules;
    }

    /**
     * Define the restore log rules that will be applied
     * by the {@link restore_logs_processor} when restoring
     * course logs. It must return one array
     * of {@link restore_log_rule} objects
     *
     * Note this rules are applied when restoring course logs
     * by the restore final task, but are defined here at
     * activity level. All them are rules not linked to any module instance (cmid = 0)
     */
    public static function define_restore_log_rules_for_course() {
        $rules = [];

        $rules[] = new restore_log_rule('applaunch', 'view all', 'index.php?id={course}', null);

        return $rules;
    }
}
